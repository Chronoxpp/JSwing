    private void lstFotosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstFotosValueChanged
        int itemSelecionado = lstFotos.getSelectedIndex();
        
        if (itemSelecionado == 0)
        {
            lblImagem.setIcon(new ImageIcon("src/image/sair.png"));
        }
        else if(itemSelecionado == 1)
        {
            lblImagem.setIcon(new ImageIcon("src/image/icons8-fornecedor-48.png"));
        }
        else if(itemSelecionado == 2)
        {
            lblImagem.setIcon(new ImageIcon("src/image/icons8-gest√£o-de-cliente-48.png"));
        }
        else if(itemSelecionado == 3)
        {
            lblImagem.setIcon(new ImageIcon("src/image/icons8-java-48.png"));
        }
        else
        {
            lblImagem.setIcon(null);
        }
        
        if(lblImagem.getIcon() == null)
        {
            lblImagem.setText("Imagem");
        }
        else
        {
            lblImagem.setText("");
        }
    }//GEN-LAST:event_lstFotosValueChanged

